<h1>Introducción</h1>
<p>En este blog podrás leer acerca del origen y el proceso de desarrollo de este proyecto</p>
<hr>
<p>Todo empezó cuando empecé a adentrarme en el mundo de la programación por puro gusto personal, después de aprender Python a 
        nivel básico para hacer programas de física computacional; simulaciones, en la universidad. Recuerdo un proyecto en el que 
        mi compañero y yo simulamos el efecto "banana" del tenis profesional, conocido por Rafael Nadal, un efecto muy extraño a 
        priori en la práctica, pero bien definido por ecuaciones de movimiento conocidas.
        La simulación se llevó a cabo con Python, librerías matemáticas para manejar gráficas de datos y una librería para dibujar
        gráficas tridimensionales donde se podían añadir pequeñas preformas, como cilindros, planos con textura, etc.
        Después de pelearnos con la librería, mi compañero y yo, puros aprendices de Python, logramos dibujar en 3D una pequeña maqueta
        de pista de tenis tridimensional rotable, que permitía apreciar mejor el efecto sobre la bola de tenis de la que estamos hablando.
        Fue en ese punto donde empecé a interesarme por los gráficos en los vdeojuegos a nivel de programación.
</p>
<p>
        Originalmente me interesé en Java como lenguaje de programación, y como los videojuegos siempre han sido una pasión para mí,
        me adentré en el mundo de la programación de gráficos 3D. Comencé aprendiendo a utilizar OpenGL con Java mediante la librería
        JOGL. La elección del lenguaje fue totalmente arbitraria. Quería probar
        algún lenguaje distinto a Python para comprobar las diferencias, y me sorprendí al ver que a nivel general, tenían la misma 
        estructura: estructuras condicionales, bucles, variables, etc. Fue entonces cuando empecé a dedicar más tiempo a Java de forma
         autodidacta a través de internet.
         Cabe mencionar aquí un par de referencias importantes que tuve en el momento de plantear el proyecto:
<ul>
    <li>Un libro que me respondió a una extensa cantidad de preguntas sobre los gráficos en los videojuegos:
        <q>Computer Graphics Programming in OpenGL with Java</q>
        <a href="https://www.amazon.es/Computer-Graphics-Programming-OpenGL-English-ebook/dp/B07HNF4YSN" target="_blank">Enlace al libro</a></li>
    <li>Y la página <a href="https://webglfundamentals.org/" target="_blank">WebGLFundamentals</a>, una página que inspiró mi idea para
                el proyecto de fin de ciclo y al mismo tiempo también contiene información muy útil sobre WebGL.</li>
</ul>
<em>Y la idea que surgió sobre el tema se trata de BloomJS, una aplicación web que permitiría a los desarrolladores web generar gráficos 3D para sus páginas, 
         mediante la ayuda auxiliar de software de código abierto para la realización de los modelos 3D.</em>
<p>
        A raíz de ir aprendiendo Java, con el tiempo empecé a interesarme más y más en el aspecto de programación de los videojuegos
        que jugaba. Pensaba cosas cómo ¿Cómo será el algoritmo que controla el movimiento de los enemigos para que parezcan tan
        inteligentes? Y de repente, me empezaron a interesar todas las cosas de las que nos rodeamos constantemente. ¿Cómo funcionan
        las páginas web? ¿Cómo se podría crear un sistema de almacenamiento seguro de información (Bases de Datos)?, etc.
        En ese momento descubrí que cerca de donde vivía se impartía el Ciclo Formativo de Desarrollo de Aplicaciones Web, y viendo
        que en el ciclo se impartían Java y JavaScript, decidí estudiarlo.
</p>
<p>Y así hemos llegado a esta página: mi proyecto para fin de ciclo del CFGS DAW, impartido en el <a href="https://ibq.es/" target="_blank">IES Bernaldo 
        de Quirós
</a></p>